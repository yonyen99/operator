function sumNumber(){
    let num1 = document.getElementById("num1").value;
    let num2 = document.getElementById("num2").value;
    let sum = 0;
    sum = Number(num1)+Number(num2);
    alert(sum);
}

function minusNumber(){
    let num1 = document.getElementById("num1").value;
    let num2 = document.getElementById("num2").value;
    let minus = 0;
    minus = Number(num1)-Number(num2);
    alert(minus);
}